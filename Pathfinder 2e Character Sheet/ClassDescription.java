
abstract class ClassDescription {
	
	private String perception;
	private String fortSavingThrow;
	private String reflexSavingThrow;
	private String willSavingThrow;
	private String classSkill1;
	private String classSkill2;
	private String extraSkills;
	private int proficiencySimple;
	private int proficiencyMartial;
	private int proficiencyClass;
	private int proficiencyLight;
	private int proficiencyMedium;
	private int proficiencyHeavy;
	private int proficiencyUnarmored;
	private int spellTraining;
	private int spellDCTraining;
	
	protected ClassDescription() {
		
	}
	
	public String getPerception() {
		return perception;
	}
	public String getFortSavingThrow() {
		return fortSavingThrow;
	}
	public String getReflexSavingThrow() {
		return reflexSavingThrow;
	}
	public String getWillSavingThrow() {
		return willSavingThrow;
	}
	public String getClassSkill1() {
		return classSkill1;
	}
	public String getClassSkill2() {
		return classSkill2;
	}
	public String getExtraSkills() {
		return extraSkills;
	}
	public int getProficiencySimple() {
		return proficiencySimple;
	}
	public int getProficiencyMartial() {
		return proficiencyMartial;
	}
	public int getProficiencyClass() {
		return proficiencyClass;
	}
	public int getProficiencyLight() {
		return proficiencyLight;
	}
	public int getProficiencyMedium() {
		return proficiencyMedium;
	}
	public int getProficiencyHeavy() {
		return proficiencyHeavy;
	}
	public int getProficiencyUnarmored() {
		return proficiencyUnarmored;
	}
	public int getSpellTraining() {
		return spellTraining;
	}
	public int getSpellDCTraining() {
		return spellDCTraining;
	}
	
}
